package home_work_1;

public class MathOperators {
    public static void main(String[] args) {
        System.out.println("PI = "+Math.PI);
        System.out.println("E = "+Math.E);
        System.out.println("decrement 10 = "+Math.decrementExact(10));
        System.out.println("abs -42 = "+Math.abs(-42));
        System.out.println("addexact 20 40 = "+Math.addExact(20, 40));
        System.out.println("hypot 5 3 = "+Math.hypot(5, 3));
        System.out.println("max between 1 and 9 = "+Math.max(1,9));
        System.out.println("multiplyExact 4 5 = "+Math.multiplyExact(4,5));
        System.out.println("nextAfter 3 6 = "+Math.nextAfter(3, 6));
        System.out.println("pow 3 2 = "+Math.pow(3,2));
        System.out.println("random number = "+Math.random());
    }
}
